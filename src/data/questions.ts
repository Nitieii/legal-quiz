// Hàm để tạo câu hỏi tương tự
export function generateSimilarQuestion(originalQuestion: Question): Question {
  const similarQuestions: Question[] = [
    {
      id: originalQuestion.id + 100,
      question: `Liên quan đến ${originalQuestion.question.toLowerCase()}, điều nào sau đây là đúng?`,
      options: [...originalQuestion.options].sort(() => Math.random() - 0.5),
      correctAnswer: originalQuestion.options.indexOf(originalQuestion.options[originalQuestion.correctAnswer]),
      explanation: originalQuestion.explanation
    },
    {
      id: originalQuestion.id + 200,
      question: `Theo quy định của Bộ Quy tắc Đạo đức và ứng xử nghề nghiệp luật sư Việt Nam về ${originalQuestion.question.toLowerCase()}, phương án nào là chính xác?`,
      options: [...originalQuestion.options].sort(() => Math.random() - 0.5),
      correctAnswer: originalQuestion.options.indexOf(originalQuestion.options[originalQuestion.correctAnswer]),
      explanation: originalQuestion.explanation
    }
  ];

  return similarQuestions[Math.floor(Math.random() * similarQuestions.length)];
}

// Hàm để lấy tất cả câu hỏi
export function getRandomQuestions(): Question[] {
  // Create a copy of the questions array to avoid mutating the original
  const shuffledQuestions = [...questions];

  // Fisher-Yates shuffle algorithm
  for (let i = shuffledQuestions.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffledQuestions[i], shuffledQuestions[j]] = [shuffledQuestions[j], shuffledQuestions[i]];
  }

  return shuffledQuestions;
}
